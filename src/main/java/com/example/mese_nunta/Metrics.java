package com.example.mese_nunta;

public class Metrics {
    private int totalInvites;
    private int totalArrived;
    private int toArrive;

    public int getTotalInvites() {
        return totalInvites;
    }

    public void setTotalInvites(int totalInvites) {
        this.totalInvites = totalInvites;
    }

    public int getTotalArrived() {
        return totalArrived;
    }

    public void setTotalArrived(int totalArrived) {
        this.totalArrived = totalArrived;
    }

    public int getToArrive() {
        return toArrive;
    }

    public void setToArrive(int toArrive) {
        this.toArrive = toArrive;
    }
}
